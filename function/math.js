console.log('math round value ' + Math.round(7.5))
console.log('math ceil value ' + Math.ceil(7.3))
console.log('math floor value ' + Math.floor(7.7))
console.log('math trunc value ' + Math.trunc(7.7))
console.log('math sign value ' + Math.sign(-7))
console.log('math pow value ' + Math.pow(7,3))
console.log('math sqrt value ' + Math.sqrt(144))
console.log('math abs value ' + Math.abs(-144))

//  math.round: returns the nearest integer 
//  math.ceil: returns the value rounded up to its nearest integer 
//  Math.floor: returns the value rounded down to its nearest integer  
//  Math.trunc returns the integer part  
//  Math.sign returns if is negative, null or positive 
//  Math.pow(x, y) returns the value of x to the power of y 
//  Math.sqrt(x) returns the square root of x: 
//  Math.abs(x) returns the absolute (positive) value of x 